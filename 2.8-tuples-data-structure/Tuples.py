# tuples are a sequence and are immutable (cannot delete or update items)

tup = ("oranges", "apples", "bananas")

# get first item of tuple
tup[0]

# get first two items of tuple
tup[0:2]

# can add two tuples together
tup2 = (12, 14)
tup + tup2

# delete entire tuple
del tup

#length of tuple
len(tup2)

#multiply
tup3 = ("HA")
tup3 * 4