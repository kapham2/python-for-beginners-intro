# conditional statements, if this else that

if (5 > 3):
    print("Hello")
else:
    print("Goodbye")

# six relational operators: > < >= <= == !=

age = 32

if (age < 18):
    print("You are young.")
elif (age >= 18 and age < 30):
    print("You are an adult.")
else:
    print("You are old.")

